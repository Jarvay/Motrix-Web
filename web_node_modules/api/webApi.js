import {axiosInstance} from './axiosInstance'

export class WebApi {
  savePreference (preference) {
    const data = {}
    for (const pKey in preference) {
      for (const key in preference[pKey]) {
        data[key] = preference[pKey][key]
      }
    }
    return axiosInstance.post('/preference', data)
  }

  getPreference () {
    return axiosInstance.get('/preference')
  }

  deleteFiles (files) {
    return axiosInstance.post('/deleteFiles', { files })
  }
}
