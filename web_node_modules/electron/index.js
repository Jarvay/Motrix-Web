import { preferenceApi } from 'api'

async function handleCommand (key, data) {
  switch (key) {
  case 'application:save-preference':
    return await preferenceApi.savePreference(data)
  }
}

export const ipcRenderer = {
  invoke: async (str) => {
    switch (str) {
    case 'get-app-config':
      return (await preferenceApi.getPreference()).data
    }
  },
  send: async (channel, ...args) => {
    switch (channel) {
    case 'command':
      handleCommand(args[0], args[1])
    }
  },

  on: () => {}
}
