import { webApi } from 'api'

async function handleCommand (key, data) {
  switch (key) {
  case 'application:save-preference':
    return await webApi.savePreference(data)
  }
}

export const ipcRenderer = {
  invoke: async (str) => {
    switch (str) {
    case 'get-app-config':
      return await webApi.getPreference()
    }
  },
  send: async (channel, ...args) => {
    switch (channel) {
    case 'command':
      await handleCommand(args[0], args[1])
    }
  },

  on: () => {}
}
